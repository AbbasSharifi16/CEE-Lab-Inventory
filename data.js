// Function to calculate buying date based on age in years and current date
function calculateBuyingDate(ageInYears) {
    if (ageInYears === '' || isNaN(ageInYears)) {
        return '';
    }
    // Using current date as June 13, 2025 as per the initial prompt.
    const today = new Date(2025, 5, 13); // Month is 0-indexed, so 5 is June
    const buyingYear = today.getFullYear() - parseInt(ageInYears, 10);
    // Return a date string for January 1st of the calculated buying year
    return `${buyingYear}-01-01`;
}

